# ----------------------------- PDF -----------------------------
class HRule(Flowable):
    def __init__(self, width, color=ORANGE, thickness=2):
        super().__init__(); self.width = width; self.color = color; self.thickness = thickness
    def draw(self):
        self.canv.setStrokeColor(self.color); self.canv.setLineWidth(self.thickness)
        self.canv.line(0, 0, self.width, 0)


def _styles():
    s = getSampleStyleSheet()
    s.add(ParagraphStyle("H1", parent=s["Heading1"], textColor=NAVY, fontSize=16, spaceBefore=10, spaceAfter=8))
    s.add(ParagraphStyle("H2", parent=s["Heading2"], textColor=ORANGE, fontSize=12, spaceBefore=8, spaceAfter=4))
    s.add(ParagraphStyle("Body2", parent=s["BodyText"], fontSize=9.5, leading=14, alignment=TA_JUSTIFY))
    s.add(ParagraphStyle("Small", parent=s["BodyText"], fontSize=8, textColor=GREY))
    return s


def _header_footer(canvas, doc):
    canvas.saveState()
    w, h = A4
    # header band (skip cover - page 1)
    if doc.page > 1:
        canvas.setFillColor(NAVY)
        canvas.rect(0, h - 1.4 * cm, w, 1.4 * cm, fill=1, stroke=0)
        canvas.setFillColor(colors.white)
        canvas.setFont("Helvetica-Bold", 9)
        canvas.drawString(1.5 * cm, h - 0.9 * cm, "RailPlan AI  ·  Detailed Project Report")
        canvas.setFont("Helvetica", 8)
        canvas.drawRightString(w - 1.5 * cm, h - 0.9 * cm, D.PROJECT["name"])
        canvas.setFillColor(ORANGE)
        canvas.rect(0, h - 1.45 * cm, w, 0.06 * cm, fill=1, stroke=0)
    # footer
    canvas.setStrokeColor(LIGHT); canvas.setLineWidth(0.5)
    canvas.line(1.5 * cm, 1.2 * cm, w - 1.5 * cm, 1.2 * cm)
    canvas.setFillColor(GREY); canvas.setFont("Helvetica", 7.5)
    canvas.drawString(1.5 * cm, 0.8 * cm, "Generated by RailPlan AI  ·  CONFIDENTIAL  ·  Demo Document")
    canvas.drawRightString(w - 1.5 * cm, 0.8 * cm, f"Page {doc.page}")
    canvas.restoreState()


def _tbl(data, col_widths, header=True, font=8, align_right=None):
    t = Table(data, colWidths=col_widths, repeatRows=1 if header else 0)
    style = [
        ("FONTSIZE", (0, 0), (-1, -1), font),
        ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
        ("GRID", (0, 0), (-1, -1), 0.4, colors.HexColor("#cbd5e1")),
        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, LIGHT]),
        ("TOPPADDING", (0, 0), (-1, -1), 3), ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
        ("LEFTPADDING", (0, 0), (-1, -1), 4), ("RIGHTPADDING", (0, 0), (-1, -1), 4),
    ]
    if header:
        style += [
            ("BACKGROUND", (0, 0), (-1, 0), NAVY),
            ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
            ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ]
    if align_right:
        for c in align_right:
            style.append(("ALIGN", (c, 0), (c, -1), "RIGHT"))
    t.setStyle(TableStyle(style))
    return t


