    E.append(Spacer(1, 1.0 * cm))
    E.append(Paragraph('<para align="center"><font color="#64748b" size="11">GOVERNMENT OF INDIA · MINISTRY OF RAILWAYS</font></para>', s["BodyText"]))
    E.append(Spacer(1, 0.3 * cm))
    E.append(Paragraph(f'<para align="center"><font color="#003087" size="26"><b>DETAILED PROJECT REPORT</b></font></para>', s["BodyText"]))
    E.append(Spacer(1, 0.2 * cm))
    E.append(Table([[""]], colWidths=[6 * cm], style=[("LINEBELOW", (0, 0), (-1, -1), 2.5, ORANGE)]))
    E.append(Spacer(1, 0.8 * cm))
