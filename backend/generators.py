def build_route_kml() -> str:
    coords = [
        (21.2514, 81.6296, "Raipur"),
        (21.0900, 81.5400, "Abhanpur"),
        (21.1450, 81.3900, "Dharsiwa"),
        (21.1900, 81.2833, "Durg"),
    ]
    line = " ".join(f"{lo},{la},0" for la, lo, _ in coords)
    placemarks = "".join(
        f'<Placemark><name>{n}</name><Point><coordinates>{lo},{la},0</coordinates></Point></Placemark>'
        for la, lo, n in coords)
    return f"""<?xml version="1.0" encoding="UTF-8"?>
<kml xmlns="http://www.opengis.net/kml/2.2">
  <Document>
    <name>RailPlan AI - {D.PROJECT['name']}</name>
    <description>{D.PROJECT['route']} - {D.PROJECT['distance_km']} km - Generated by RailPlan AI</description>
    <Style id="routeB"><LineStyle><color>ff00cc4c</color><width>4</width></LineStyle></Style>
    <Placemark>
      <name>Route B - AI Optimized</name>
      <styleUrl>#routeB</styleUrl>
      <LineString><tessellate>1</tessellate><coordinates>{line}</coordinates></LineString>
    </Placemark>
    {placemarks}
  </Document>
</kml>"""
