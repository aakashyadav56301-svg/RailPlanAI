# ----------------------------- Excel BOQ -----------------------------
def build_boq_xlsx() -> bytes:
    from openpyxl import Workbook
    from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
    from openpyxl.utils import get_column_letter

    wb = Workbook()
    ws = wb.active
    ws.title = "BOQ"
    navy = "FF003087"; orange = "FFFF6B00"; light = "FFF1F5F9"
    thin = Side(style="thin", color="FFCBD5E1")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)

    # title
    ws.merge_cells("A1:F1")
    ws["A1"] = "RailPlan AI — Bill of Quantities"
    ws["A1"].font = Font(bold=True, size=15, color="FFFFFFFF")
    ws["A1"].fill = PatternFill("solid", fgColor=navy)
    ws["A1"].alignment = Alignment(horizontal="center", vertical="center")
    ws.row_dimensions[1].height = 26
    ws.merge_cells("A2:F2")
    ws["A2"] = f"{D.PROJECT['name']}  ·  {D.PROJECT['route']}  ·  {D.PROJECT['distance_km']} km"
    ws["A2"].font = Font(italic=True, size=10, color="FF475569")
    ws["A2"].alignment = Alignment(horizontal="center")

    headers = ["Sr", "Item", "Unit", "Qty", "Rate (Rs.)", "Amount (Rs. Cr)"]
    ws.append([])
    ws.append(headers)
    hr = ws.max_row
    for c in range(1, 7):
        cell = ws.cell(row=hr, column=c)
        cell.font = Font(bold=True, color="FFFFFFFF")
        cell.fill = PatternFill("solid", fgColor=orange)
        cell.alignment = Alignment(horizontal="center", vertical="center")
        cell.border = border

    total = 0.0
    for sr, item, unit, qty, rate, amt in D.BOQ:
        ws.append([sr, item, unit, qty, (f"{rate:,}" if isinstance(rate, int) else rate), float(amt)])
        total += float(amt)
        r = ws.max_row
        for c in range(1, 7):
            cell = ws.cell(row=r, column=c); cell.border = border
            if r % 2 == 0:
                cell.fill = PatternFill("solid", fgColor=light)
        ws.cell(row=r, column=6).number_format = '#,##0.00'

    # total row
    ws.append(["", "TOTAL", "", "", "", round(total, 2)])
    tr = ws.max_row
    for c in range(1, 7):
        cell = ws.cell(row=tr, column=c)
        cell.font = Font(bold=True, color="FFFFFFFF"); cell.fill = PatternFill("solid", fgColor=navy)
        cell.border = border
    ws.cell(row=tr, column=6).number_format = '#,##0.00'

    widths = [6, 46, 8, 14, 16, 16]
    for i, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = w
    ws.freeze_panes = "A5"

    buf = io.BytesIO(); wb.save(buf); return buf.getvalue()


