    E.append(Spacer(1, 0.2 * cm))
    E.append(Paragraph(f'<para align="center"><font size="12" color="#475569">{P["route"]}  ·  {P["distance_km"]} km  ·  {P["gauge"]}</font></para>', s["BodyText"]))
